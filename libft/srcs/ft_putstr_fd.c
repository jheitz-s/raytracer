#include "libft.h"
#include <unistd.h>

void	ft_putstr_fd(const char *s, int fd)
{
	if (s != 0 && *s != '\0')
	{
		write(fd, s, 1);
		ft_putstr_fd(s + 1, fd);
	}
}
